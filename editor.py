from models import Shape

class VectorEditor:
    """
    Класс-контроллер. Отвечает за хранение списка фигур и
    взаимодействие с ними. Мы отделяем логику хранения от интерфейса (CLI).
    """
    def __init__(self):
        # Храним фигуры в обычном списке. Для текущих задач
        # поиска по индексу и итерации этого более чем достаточно.
        self._shapes = []

    def add_shape(self, shape: Shape):
        """Добавляет готовую фигуру в общий список."""
        self._shapes.append(shape)
        print(f"[Успех] Фигура добавлена: {shape}")

    def get_all_shapes(self):
        """Возвращает список всех фигур. Используется для вывода в CLI."""
        return self._shapes

    def delete_shape(self, index: int):
        """
        Удаляет фигуру по её порядковому номеру.
        Добавлена проверка индекса, чтобы программа не 'падала' при ошибке ввода.
        """
        try:
            # Проверяем, существует ли такой индекс в списке
            removed = self._shapes.pop(index)
            print(f"[Успех] Удалена фигура: {removed}")
        except IndexError:
            print(f"[Ошибка] Фигуры с номером {index} не существует.")
        except ValueError:
            print("[Ошибка] Введите корректное числовое значение индекса.")